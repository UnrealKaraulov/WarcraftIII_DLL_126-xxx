typedef std::unordered_map<unsigned long, unsigned long> VersionAddressMapType;
typedef std::unordered_map<int, VersionAddressMapType> OffsetMapType;

static unsigned long VersionId;
static unsigned long Base;

static OffsetMapType OffsetMap;

bool InitMap();

bool Offset_Init(unsigned long version, unsigned long base) {
	VersionId = version;
	Base = base;
	return InitMap();
}

unsigned char* Offset(int offset) {
	unsigned char* rv = NULL;
	VersionAddressMapType::iterator iter;
	if (OffsetMap[offset].end() != (iter = OffsetMap[offset].find(
		VersionId)
		))
	{
		unsigned long address = iter->second;
		//address = (~(address ^ OFFSET_ENCRYPT_KEY)) - OFFSET_ENCRYPT_ADD;
		rv = (unsigned char*)(address + Base);
	}
	return rv;
}

